    ssd1306_write(0x00, 0xAE); // Display off
    ssd1306_write(0x00, 0xD5); // Set display clock divide ratio/oscillator frequency
    ssd1306_write(0x00, 0x80); // Set divide ratio
    ssd1306_write(0x00, 0xA8); // Set multiplex ratio
    ssd1306_write(0x00, 0x3F); // 1/64 duty
    ssd1306_write(0x00, 0xD3); // Set display offset
    ssd1306_write(0x00, 0x00); // No offset
    ssd1306_write(0x00, 0x40); // Set start line address
    ssd1306_write(0x00, 0x8D); // Charge pump setting
    ssd1306_write(0x00, 0x14); // Enable charge pump
